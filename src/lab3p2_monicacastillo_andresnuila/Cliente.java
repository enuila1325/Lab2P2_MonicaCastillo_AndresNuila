package lab3p2_monicacastillo_andresnuila;

import java.util.ArrayList;
import java.util.Date;

public class Cliente extends Persona {

    private double money;
    private ArrayList compras = new ArrayList();

    public Cliente() {
    }

    public Cliente(int ID, String contraseña, String correo, String nombre, Date nacimiento, double money, String username) {
        super(username, ID, contraseña, correo, nombre, nacimiento);
        this.money = money;
    }

    public double getMoney() {
        return money;
    }

    public void setMoney(double money) {
        this.money = money;
    }

    public ArrayList getCompras() {
        return compras;
    }

    public void setCompras(ArrayList compras) {
        this.compras = compras;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Date getNacimiento() {
        return nacimiento;
    }

    public void setNacimiento(Date nacimiento) {
        this.nacimiento = nacimiento;
    }

    @Override
    public String toString() {
        return super.toString() + "money=" + money + ", compras=" + compras + '}';
    }

}
